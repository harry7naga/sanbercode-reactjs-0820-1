function fungsiLuasPersegi(p, l) {
  return p * l
}
console.log("--- Luas Persegi ---")
console.log(fungsiLuasPersegi(5, 3))

function fungsiLuasLingkaran(r) {
  return 3.14 * r * r
}
console.log("--- Luas lingkaran ---")
console.log(fungsiLuasLingkaran(7))

function fungsiLuasSegitiga(a, t) {
  return a * t * 0.5
}
console.log("--- Luas Segitiga ---")
console.log(fungsiLuasSegitiga(4, 3))